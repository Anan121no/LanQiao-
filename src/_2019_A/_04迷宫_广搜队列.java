public class _04迷宫_广搜队列 {
	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);//将注释的迷宫输入得到结果
		Queue<Integer> queue = new LinkedList<Integer>();
		int n=30,m=50;
		char[][] maze = new char[30][50];
		char[] c = { 'D', 'L', 'R', 'U' };
		int[][] dis = new int[30][50];
		int[][] dir = { { 1, 0 }, { 0, -1 }, { 0, 1 }, { -1, 0 } };
		for (int i = 0; i < n; i++) {
			String string = in.next();
			for (int j = 0; j < m; j++)
				maze[i][j] = string.charAt(j);
		}
		queue.add((n - 1) * m + m - 1);
		while (!queue.isEmpty()) {
			int temp = queue.poll();
			for (int i = 0; i < 4; i++) {
				int xx = temp / m + dir[i][0];
				int yy = temp % m + dir[i][1];
				if (xx < 0 || xx >= n || yy < 0 | yy >= m || maze[xx][yy] == '1' || dis[xx][yy] != 0) {
					continue;
				}
				queue.add(xx * m + yy);
				dis[xx][yy] = dis[temp / m][temp % m] + 1;
				if (xx == 0 && yy == 0)
					break;
			}
		}
		dis[n - 1][m - 1] = 0;
		String path = "";
		int x = 0, y = 0;
		while (x != n - 1 || y != m - 1) {
			for (int i = 0; i < 4; i++) {
				int xx = x + dir[i][0];
				int yy = y + dir[i][1];
				if (xx < 0 || xx >= n || yy < 0 | yy >= m || maze[xx][yy] == '1')
					continue;
				if (dis[x][y] == dis[xx][yy] + 1) {
					x = xx;
					y = yy;
					path += c[i];
					break;
				}
			}
		}
		System.out.println(path);
	}
}
