public class _03数列求值 {
	public static void main(String[] args) {
		int arr[] = new int[30000000];
		arr[0] = 1;
		arr[1] = 1;
		arr[2] = 1;
		for (int i = 3; i <= 20190324; i++) {
			arr[i] = arr[i-1]+arr[i-2]+arr[i-3];
			arr[i] = arr[i]%10000;
		}
		System.out.println(arr[6]);
		System.out.println(arr[20190323]);
	}
}
