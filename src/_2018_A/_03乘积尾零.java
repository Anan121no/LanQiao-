public class _03乘积尾零 {
		public static void main(String[] args) {
	int sum1=0,sum2=0,sum;
	int n;
	Scanner scan = new Scanner(System.in);
	for(int i=0;i<100;i++) {
//		System.out.println("第"+i+1+"次输入");
		n = scan.nextInt();
		while(n%5==0) {
			sum1++;
			n=n/5;
		}
		while(n%2==0) {
			sum2++;
			n=n/2;
		}
		
	}
	
	if(sum1<sum2) {
		sum = sum1;
	}else {
		sum=sum2;
	}
	System.out.println(sum);
		}
}
