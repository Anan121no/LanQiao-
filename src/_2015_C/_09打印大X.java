public class _09打印大X {
	public static void main(String[] args) {
		Scanner in =new Scanner(System.in);
		int m,n;
		m = in.nextInt();
		n = in.nextInt();
		int w = m+(n/2)*2;
		int g[][] = new int [n][w];
		for (int j = 0; j < n; j++) {
			for (int i = 0; i < w; i++) {
				g[j][i]='.';
			}
		}
		//从左上往右下
		for (int i = 0; i < n; i++) {
			for (int k = 0; k < m; k++) {
				g[i][i+k]='*';
			}
		}
		//从右上往左下
		for (int l = 0; l < n; l++) {
			for (int i = 0; i < m; i++) {
				g[l][w-1-l-i]='*';
			}
		}
		for (int i = 0; i < n; i++) {
			for (int j = 0; j < w; j++) {
				System.out.printf("%c",g[i][j]);
			}
			System.out.printf("\n");
		}
	}
}
